""" * Crie um programa que calcula o volume de uma caixa retargular.
* Use as seguintes dimensões:

* Comprimento: 12 cm
* Largura: 14 cm
Altura: 20 cm

*O programa deve calcular o volume e exibir o resultado em cm3.

"""

comprimento= 12
largura= 14
altura= 20

volume= comprimento * largura * altura

print(f"Comprimento:{comprimento} cm")
print(f"Largura:{largura} cm")
print(f"Altura:{altura} cm")
print(f"O volume da caixa retangular é de {volume} cm³")